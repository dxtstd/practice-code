public class OperationMath {
  public static void main(String[] args) {

    int a = 100;
    int b = 10;

    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a * b);
    System.out.println(a / b);
    System.out.println(a % b);

    // Augmented Assignments

    int c = 100;

    c += 10;
    System.out.println(c);

    c -= 10;
    System.out.println(c);

    c *= 10;
    System.out.println(c);

    /*
    c++ or ++c result c = c + 1;
    c-- or --c result c = c - 1;
    */

  }
}
